package it.eng.pathway.fake.model;

public class HRZone {

	private Integer minimumHR;

	private Integer maximumHR;
	
	
	

	public HRZone(Integer minBpm, Integer maxBpm) {
		super();
		this.minimumHR = minBpm;
		this.maximumHR = maxBpm;
	}

	public HRZone() {
		super();
	}

	public Integer getMinimumHR() {
		return minimumHR;
	}

	public void setMinimumHR(Integer minimumHR) {
		this.minimumHR = minimumHR;
	}

	public Integer getMaximumHR() {
		return maximumHR;
	}

	public void setMaximumHR(Integer maximumHR) {
		this.maximumHR = maximumHR;
	}
	
	

}
